use crate::memory::{AppState, Application};

pub fn unguard<F, T>(state: &AppState, f: F) -> Result<T, String>
where
    F: FnOnce(&mut Application) -> Result<T, String>,
{
    let mut guard = state.lock().map_err(|_| "Failed to lock mutex")?;
    let memory = guard.as_mut().ok_or("Game process not attached")?;
    f(memory)
}

pub fn render_template(template: Option<&str>, data: &serde_json::Value) -> String {
    let reg = handlebars::Handlebars::new();

    let result = if let Some(template_str) = template {
        match reg.render_template(template_str, data) {
            Ok(s) => s,
            Err(e) => {
                eprintln!("Template Error: {}", e);
                "Config Error".to_string()
            }
        }
    } else {
        "".to_string()
    };

    result
}

